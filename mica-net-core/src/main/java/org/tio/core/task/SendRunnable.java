package org.tio.core.task;

import java.nio.ByteBuffer;
import java.util.ArrayList;
import java.util.List;
import java.util.Queue;
import java.util.concurrent.ConcurrentLinkedQueue;
import java.util.concurrent.Executor;
import java.util.concurrent.atomic.AtomicBoolean;

import javax.net.ssl.SSLException;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.tio.core.ChannelContext;
import org.tio.core.ChannelContext.CloseCode;
import org.tio.core.TcpConst;
import org.tio.core.Tio;
import org.tio.core.TioConfig;
import org.tio.core.WriteCompletionHandler.WriteCompletionVo;
import org.tio.core.intf.Packet;
import org.tio.core.intf.TioHandler;
import org.tio.core.ssl.SslUtils;
import org.tio.core.ssl.SslVo;
import org.tio.core.utils.TioUtils;
import org.tio.utils.thread.pool.AbstractQueueRunnable;

/**
 * @author tanyaowu
 * 2017年4月4日 上午9:19:18
 */
public class SendRunnable extends AbstractQueueRunnable<Packet> {
	private static final Logger log = LoggerFactory.getLogger(SendRunnable.class);
	private static final int MAX_CAPACITY_MIN = TcpConst.MAX_DATA_LENGTH - 1024;    //减掉1024是尽量防止溢出的一小部分还分成一个tcp包发出
	private static final int MAX_CAPACITY_MAX = MAX_CAPACITY_MIN * 10;
	private final ChannelContext channelContext;
	private final TioConfig tioConfig;
	private final TioHandler tioHandler;
	private final boolean isSsl;
	/**
	 * The msg queue.
	 */
	private final Queue<Packet> msgQueue;
	/**
	 * 异步优化：标记是否有写操作正在进行
	 * 用于防止 AsynchronousSocketChannel 的 WritePendingException
	 */
	private final AtomicBoolean writing = new AtomicBoolean(false);
	/**
	 * The msg queue.
	 */
	private ConcurrentLinkedQueue<Packet> forSendAfterSslHandshakeCompleted = null;

	public SendRunnable(ChannelContext channelContext, Executor executor) {
		this(channelContext, executor, new ConcurrentLinkedQueue<>());
	}

	public SendRunnable(ChannelContext channelContext, Executor executor, Queue<Packet> msgQueue) {
		super(executor);
		this.channelContext = channelContext;
		this.tioConfig = channelContext.tioConfig;
		this.tioHandler = tioConfig.getTioHandler();
		this.isSsl = SslUtils.isSsl(tioConfig);
		this.msgQueue = msgQueue;
	}

	public Queue<Packet> getForSendAfterSslHandshakeCompleted(boolean forceCreate) {
		if (forSendAfterSslHandshakeCompleted == null && forceCreate) {
			synchronized (this) {
				if (forSendAfterSslHandshakeCompleted == null) {
					forSendAfterSslHandshakeCompleted = new ConcurrentLinkedQueue<>();
				}
			}
		}
		return forSendAfterSslHandshakeCompleted;
	}

	@Override
	public boolean addMsg(Packet packet) {
		if (this.isCanceled()) {
			log.info("{}, 任务已经取消，{}添加到发送队列失败", channelContext, packet.logstr());
			return false;
		}

		// ⭐ 预编码优化：在入队前完成编码，避免在发送线程中编码
		// 这样可以将编码工作分散到多个业务线程，提升发送线程的处理效率
		if (packet.getPreEncodedByteBuffer() == null) {
			try {
				ByteBuffer encoded = tioHandler.encode(packet, tioConfig, channelContext);
				if (encoded != null) {
					// 确保 ByteBuffer 处于可读状态
					if (!encoded.hasRemaining()) {
						encoded.flip();
					}
					packet.setPreEncodedByteBuffer(encoded);
				}
			} catch (Exception e) {
				log.error("{}, 预编码失败: {}", channelContext, packet.logstr(), e);
				// 预编码失败也允许入队，会在 getByteBuffer 中重试
			}
		}

		if (channelContext.sslFacadeContext != null && !channelContext.sslFacadeContext.isHandshakeCompleted() && SslUtils.needSslEncrypt(packet, tioConfig)) {
			return this.getForSendAfterSslHandshakeCompleted(true).add(packet);
		} else {
			return msgQueue.add(packet);
		}
	}

	/**
	 * 清空消息队列
	 */
	@Override
	public void clearMsgQueue() {
		Packet p;
		forSendAfterSslHandshakeCompleted = null;
		while ((p = msgQueue.poll()) != null) {
			try {
				channelContext.processAfterSent(p, false);
			} catch (Throwable e) {
				log.error(e.getMessage(), e);
			}
		}
	}

	private ByteBuffer getByteBuffer(Packet packet) {
		try {
			ByteBuffer byteBuffer = packet.getPreEncodedByteBuffer();
			if (byteBuffer == null) {
				byteBuffer = tioHandler.encode(packet, tioConfig, channelContext);
			}

			if (!byteBuffer.hasRemaining()) {
				byteBuffer.flip();
			}
			return byteBuffer;
		} catch (Exception e) {
			log.error(packet.logstr(), e);
			throw new RuntimeException(e);
		}
	}

	@Override
	public void runTask() {
		// 异步优化：如果有写操作正在进行，直接返回，避免 WritePendingException
		if (writing.get()) {
			return;
		}

		if (msgQueue.isEmpty()) {
			return;
		}

		int queueSize = msgQueue.size();
		if (queueSize == 1) {
			Packet packet = msgQueue.poll();
			if (packet != null) {
				sendPacket(packet);
			}
			return;
		}

		// 动态调整批量大小：根据队列积压情况自适应调整
		// 针对 MQTT 等小消息场景，采用纯数量控制策略，简化逻辑
		int targetBatchSize = getPacketListCapacity(queueSize);

		Packet packet;
		List<Packet> packets = new ArrayList<>(targetBatchSize);
		List<ByteBuffer> byteBuffers = new ArrayList<>(targetBatchSize);
		int allBytebufferCapacity = 0;
		Boolean needSslEncrypted = null;
		boolean sslChanged = false;
		while ((packet = msgQueue.poll()) != null) {
			ByteBuffer byteBuffer = getByteBuffer(packet);

			packets.add(packet);
			byteBuffers.add(byteBuffer);
			allBytebufferCapacity += byteBuffer.limit();

			if (isSsl) {
				boolean _needSslEncrypted = !packet.isSslEncrypted();
				if (needSslEncrypted != null) {
					sslChanged = needSslEncrypted != _needSslEncrypted;
				}
				needSslEncrypted = _needSslEncrypted;
			}

			// 批量控制：按数量控制，适合 MQTT 等小消息场景
			// 1. 达到目标批量数量
			// 2. SSL 状态变化（必须分开发送）
			// 3. 数据量超过最大限制（安全上限保护，防止单批过大）
			if (packets.size() >= targetBatchSize
				|| allBytebufferCapacity >= MAX_CAPACITY_MAX
				|| sslChanged) {
				break;
			}
		}
		if (allBytebufferCapacity == 0) {
			return;
		}
		ByteBuffer allByteBuffer = ByteBuffer.allocate(allBytebufferCapacity);
		for (ByteBuffer byteBuffer : byteBuffers) {
			allByteBuffer.put(byteBuffer);
		}

		allByteBuffer.flip();

		if (isSsl && needSslEncrypted) {
			SslVo sslVo = new SslVo(allByteBuffer, packets);
			try {
				channelContext.sslFacadeContext.getSslFacade().encrypt(sslVo);
				allByteBuffer = sslVo.getByteBuffer();
			} catch (SSLException e) {
				log.error("{}, 进行SSL加密时发生了异常", channelContext, e);
				Tio.close(channelContext, "进行SSL加密时发生了异常", CloseCode.SSL_ENCRYPTION_ERROR);
				return;
			}
		}

		this.sendByteBuffer(allByteBuffer, packets);
	}

	public boolean sendPacket(Packet packet) {
		ByteBuffer byteBuffer = getByteBuffer(packet);
		if (isSsl && !packet.isSslEncrypted()) {
			SslVo sslVo = new SslVo(byteBuffer, packet);
			try {
				channelContext.sslFacadeContext.getSslFacade().encrypt(sslVo);
				byteBuffer = sslVo.getByteBuffer();
			} catch (SSLException e) {
				log.error("{}, 进行SSL加密时发生了异常", channelContext, e);
				Tio.close(channelContext, "进行SSL加密时发生了异常", CloseCode.SSL_ENCRYPTION_ERROR);
				return false;
			}
		}
		this.sendByteBuffer(byteBuffer, packet);
		return true;
	}

	/**
	 * @param byteBuffer ByteBuffer
	 * @param packets    Packet or List<Packet>
	 */
	private void sendByteBuffer(ByteBuffer byteBuffer, Object packets) {
		if (byteBuffer == null) {
			log.error("{},byteBuffer is null", channelContext);
			return;
		}
		if (!TioUtils.checkBeforeIO(channelContext)) {
			return;
		}
		// 异步优化：标记写入状态
		writing.set(true);
		try {
			WriteCompletionVo writeCompletionVo = new WriteCompletionVo(byteBuffer, packets);
			// 异步发送，不再等待完成，回调中会处理
			channelContext.asynchronousSocketChannel.write(byteBuffer, writeCompletionVo, channelContext.writeCompletionHandler);
		} catch (Exception e) {
			// 如果发送失败，恢复状态
			writing.set(false);
			log.error("{}, 异步写入失败", channelContext, e);
		}
	}

	@Override
	public String toString() {
		return this.getClass().getSimpleName() + ':' + channelContext.toString();
	}

	@Override
	public String logstr() {
		return toString();
	}

	@Override
	public Queue<Packet> getMsgQueue() {
		return msgQueue;
	}

	/**
	 * 异步优化：写操作完成时调用，由 WriteCompletionHandler 回调
	 * 清除写入标记并触发下一批消息发送
	 */
	public void onWriteCompleted() {
		writing.set(false);
		// 优化：只在未提交状态且有消息时才触发，避免无效的 execute 调用和锁竞争
		// executed 为 true 表示任务已在线程池队列中，无需重复提交
		if (!msgQueue.isEmpty() && !this.executed) {
			this.execute();
		}
	}

	/**
	 * 根据队列积压情况自适应调整批量大小
	 *
	 * @param queueSize 队列积压大小
	 * @return 批量大小
	 */
	private static int getPacketListCapacity(int queueSize) {
		// 积压越多，批量越大，加速消费；积压少时，批量小，降低延迟
		if (queueSize > 20000) {
			return 8000;                    // 极大积压：超大批量快速消化
		} else if (queueSize > 10000) {
			return 5000;                    // 严重积压：大批量处理
		} else if (queueSize > 5000) {
			return 3000;                    // 中度积压：中大批量
		} else if (queueSize > 2000) {
			return 1500;                    // 轻度积压：中批量
		} else if (queueSize > 1000) {
			return 1000;                    // 小积压：标准批量
		} else if (queueSize > 300) {
			return 500;                     // 少量积压：小批量
		} else {
			return queueSize;               // 极少消息：按实际数量
		}
	}

}
