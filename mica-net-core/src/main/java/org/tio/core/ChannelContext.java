	/**
	 * 获取指定状态位的值
	 *
	 * @param position 0~7 8位
	 */
	private boolean getState(int position) {
		return (this.states & (1 << position)) != 0;
	}

	/**
	 * 设置指定状态位的值
	 *
	 * @param position 0~7 8位
	 * @param state    状态
	 */
	private void setState(int position, boolean state) {
		if (state) {
			// 使用或运算将指定位设置为1
			this.states |= (byte) (1 << position);
		} else {
			// 使用与运算将指定位设置为0
			this.states &= (byte) ~(1 << position);
		}
	}

